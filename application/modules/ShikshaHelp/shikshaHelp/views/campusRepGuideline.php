    	<h2>Guidelines for Campus Representatives</h2>
        <p>Campus Connect is a program through which aspirants get their questions/doubts answered by Campus Representatives from India's top colleges. As a valuable member of this community you are required to follow the guidelines mentioned below while you answer a question or post a comment and help us keep this community clean. You can help so many aspirants out there with your answers. Here's how you can write the best quality answer:
</p>
        
        <div class="warning-box">
        	<i class="warning-icn"></i>Warning: Violating the Community Guidelines may result in the termination of your Shiksha Cafe account without warning. In extreme cases, violations may result in the termination of access to all other Shiksha services.</div>
        
        <div style="margin-top:22px; clear:both">
        	<div class="guideline-cols flLt">
            	<h3><i class="todo-icn"></i>What to do?</h3>
                
                <ol>
                	<li><strong>1. Make it grammatically correct and comprehensive:</strong>As a campus representative, you will come across students from different academic backgrounds. Some may have strong English language skills, while others may be weak in their understanding or expression. So, you can help them all by answering questions in simple and clear sentences that can be easily understood. Do not use SMS language. </li>
                    <li><strong>2. Ensure you answer all concerns of the student:</strong>At times, students may ask questions that may appear too narrow or too broad. It would be great for you to look into the question and answer it as comprehensively as possible so that a student is satisfied with the information provided to him or her.</li>
                    <li><strong>3. Be more detailed: </strong>While students may ask questions in an informal manner, your answers should not have a casual approach. An answer will look more impressive if it's framed well in full sentences with complete information. In fact, it's good to add related information that you feel will be useful to the students. At times, you might also realise there is some extra information that would help the students. Make sure you include that as well in your answers.</li>
                    <li><strong>4. Cite sources wherever necessary: </strong>Sometimes, it may not be possible to cover every aspect of a question in your answer. However, you can still help by providing the right references or links where students can look up for further guidance on the topic.<br/>
                    So, go ahead, make every answer, your best answer!</li>
                </ol>
            </div>
            
            <div class="guideline-cols flRt">
            	<h3><i class="not-todo-icn"></i>What NOT to do?</h3>
                <ol>
                	<li><strong>1. Providing extremely short answers: </strong>Providing extremely short answers may result in disapproval of your answers if they are found to be inadequate in explanation. Always ensure that you provide a detailed answer to the question so that it addresses all concerns of the aspirant who asked you for information. </li>
                    <li><strong>2. Misusing the platform: </strong>Don't create multiple accounts to earn or otherwise violate these Campus Representative guidelines. This may result in your permanent removal from the Campus Connect program. Don't post things that are incomprehensible or confusing. </li>
                    <li><strong>3. Providing Irrelevant/Objectionable answers:  </strong>It is strictly prohibited to post information that is false, misleading, unlawful, obscene, vulgar and objectionable or with the intent to harass, threaten, abuse, accuse or harm the feelings of anyone.</li>
                    <li><strong>4. Copyrighted Material: </strong>Posting of copyrighted material is strictly prohibited. If the information is particularly useful to students, you can cite the same as a source and use the web link to direct the community members to the original source.</li>
                </ol>
