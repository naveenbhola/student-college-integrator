               <div class="guideline-cols flLt">
                  <h3>What to do?</h3>
                  <ol>
                     <li><strong>Share Knowledge:</strong> Someone could benefit from your knowledge, opinions, or personal experiences.</li>
                     <li><strong>Practice Courtesy:</strong> Shiksha Ask 
                        &amp; Answer is a diverse community of people with diverse opinions. 
                        Showing respect to others makes the community better for all members.
                     </li>
                     <li><strong>Cite Sources:</strong> To make your answers and discussions more meaningful and credible, always try to cite online resources with your answers.</li>
                     <li><strong>Ask Clearly:</strong> Keep your 
                        questions short but descriptive enough for the expert to understand your
                        question. In case of discussions, give a meaningful title to the 
                        discussion post.
                     </li>
                     <li><strong>Avoid Chat Lingo:</strong> Keep the 
                        language of your question/comments/discussions clear and try avoiding 
                        chat lingo and short forms. This will help the experts and community 
                        members  in understanding your question clearly.
                     </li>
                     <li><strong>Categorize Correctly:</strong> 
                        Categorize your questions/discussions/announcements correctly by placing
                        them in relevant category. This will help the Shiksha Ask &amp; Answer 
                        experts in finding your question easily.
                     </li>
                  </ol>
               </div>
               <div class="guideline-cols flRt">
                  <h3></i>What not to do?</h3>
                  <ol>
                     <li><strong>Promote Business:</strong> Shiksha Ask 
                        &amp; Answer is a community for sharing knowledge. It is prohibited to 
                        use this platform for promoting business interest, self promotion, 
                        advertisement and spamming members.
                     </li>
                     <li><strong>Hateful Behavior:</strong> It is 
                        strictly prohibited to post information that is false, misleading, 
                        unlawful, obscene, vulgar and objectionable or with the intent to 
                        harass, threaten, abuse, accuse or harm the feelings of anyone.
                     </li>
                     <li><strong>Duplicate Content:</strong> Before 
                        posting any new question/discussion/announcement, please use the search 
                        box to see if the same has already been posted before. This will save 
                        your time for you and community members.
                     </li>
                     <li><strong>Copyrighted Material:</strong> Posting 
                        of copyrighted material is strictly prohibited. You can cite the same as
                        a source and use the web link to direct the community members to the 
                        original source.
                     </li>
                     <li><strong>Misusing Shiksha Ask &amp; Answer:</strong>
                        Don't create multiple accounts to earn points or otherwise violate 
                        these guidelines or the Shiksha Terms of Service. Don't post things that
                        are incomprehensible or in the wrong language.
                     </li>
                  </ol>
               </div>
