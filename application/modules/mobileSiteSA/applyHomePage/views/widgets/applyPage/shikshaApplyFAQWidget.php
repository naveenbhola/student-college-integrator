          <strong>Do I have to pay anything for using Shiksha counseling service?</strong>
          <p>Shiksha counseling services are 100% free of cost to all users. There are no hidden fees. However all students must sign an exclusivity agreement, which means they cannot hire the services of any other agent or consultant.</p>
