<P class="p0 ft0">Tag Auto­Suggestor</P>
<P class="p1 ft1">What ?</P>
<P class="p2 ft4">Tag­autosuggestor is used to show relevant suggestions to the user corresponding to the keywords typed by him/her. The relevancy of the suggestions are dependent on the <SPAN class="ft2">​</SPAN><SPAN class="ft3">logic</SPAN><SPAN class="ft2">​</SPAN>(mentioned below) implemented.</P>
<P class="p3 ft1">Logic</P>
<P class="p4 ft7">Whenever a user starts typing a phrase(suppose <SPAN class="ft5">​</SPAN><SPAN class="ft6">K</SPAN><SPAN class="ft5">​</SPAN>), we will give the boost to a Matching Tags(suppose <SPAN class="ft5">​</SPAN><SPAN class="ft6">T</SPAN><SPAN class="ft5">​</SPAN>) in the following order :</P>
<P class="p5 ft7"><SPAN class="ft7">●</SPAN><SPAN class="ft8">K</SPAN><SPAN class="ft9">​</SPAN>matches completely with <SPAN class="ft9">​</SPAN><SPAN class="ft6">T</SPAN></P>
<P class="p6 ft10">Eg. If user types “mba” then “MBA” tag will be given boost rather than “MBA Eligibility”.</P>
<P class="p7 ft7"><SPAN class="ft7">●</SPAN><SPAN class="ft8">K</SPAN><SPAN class="ft11">​</SPAN>matches completely with a synonym of <SPAN class="ft11">​</SPAN><SPAN class="ft6">T</SPAN></P>
<P class="p8 ft10">Eg. If user types “mbbch” then “MBBS” will be given boost as MBBCh is a synonym of MBBS</P>
<P class="p7 ft7"><SPAN class="ft7">●</SPAN><SPAN class="ft8">K</SPAN><SPAN class="ft11">​</SPAN>is present in the starting string of <SPAN class="ft11">​</SPAN><SPAN class="ft6">T</SPAN></P>
<P class="p9 ft10">Eg. If user types “mba” then tags like “MBA Eligibility” will be giveb boost rather than tags like “Executive MBA” ‘caz types keyword matches with the starting string of the tag.</P>
<P class="p10 ft7"><SPAN class="ft7">●</SPAN><SPAN class="ft12">All Tokens of </SPAN><SPAN class="ft11">​</SPAN><SPAN class="ft6">K</SPAN><SPAN class="ft11">​</SPAN>(tokens are words in <SPAN class="ft11">​</SPAN><SPAN class="ft6">K</SPAN><SPAN class="ft11">​</SPAN>) are present in <SPAN class="ft11">​</SPAN><SPAN class="ft6">T </SPAN>This is a filtering logic rather than boosting logic</P>
<P class="p11 ft7"><SPAN class="ft7">●</SPAN><SPAN class="ft8">K</SPAN><SPAN class="ft11">​</SPAN>matches with the starting string of a synonym of <SPAN class="ft11">​</SPAN><SPAN class="ft6">T</SPAN></P>
<P class="p12 ft7">This is same as point 2 but this applies to synonyms rather than tag name.</P>
<P class="p13 ft7"><SPAN class="ft7">●</SPAN><SPAN class="ft12">Tokens of </SPAN><SPAN class="ft11">​</SPAN><SPAN class="ft6">K</SPAN><SPAN class="ft11">​</SPAN>are present in any synonym of <SPAN class="ft11">​</SPAN><SPAN class="ft6">T</SPAN></P>
<P class="p12 ft7">This point is also similar to point 3 but applies to synonyms rather than tag name.</P>
<P class="p14 ft14"><SPAN class="ft7">●</SPAN><SPAN class="ft13">Apart from the above logic we will give boost to all non­variant tags</SPAN></P>
<P class="p15 ft7">We will be giving boost to all those tags that belongs to non­variant tag category. Eg. If a user types 'tally' in that case "<SPAN class="ft15">​</SPAN><SPAN class="ft6">Tally</SPAN><SPAN class="ft15">​</SPAN>" Tag will be given preference as it is a parent tag(type = Specialization) instead of "<SPAN class="ft15">​</SPAN><SPAN class="ft6">Tally Cut­off</SPAN><SPAN class="ft15">​</SPAN>" which is a variant tag(type = Specialization Variant)</P>
