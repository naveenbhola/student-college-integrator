<P class="p5 ft14"><SPAN class="ft7">●</SPAN><SPAN class="ft13">Quality Factor</SPAN></P>
<P class="p16 ft10">We have also decided to give a slight boost to the tags having good quality factor(a combination of number of follows, questions and discussions associated). This little boost will give the preference to the tags which are relatively popular in case of tie(same score)</P>
<P class="p17 ft10">We have defined Quality Factor of a tag on the basis of number of users following that tag + number of questions/discussion on which this tag has been tagged.</P>
<P class="p18 ft7"><SPAN class="ft6">Quality Factor </SPAN><SPAN class="ft11">​</SPAN>= (followMultiplicativeFactor * followCount) + (questionMultiplicativeFactor * questionsLinkedCount) + (discussionMultiplicativeFactor * discussionLinkedCount)</P>
<P class="p19 ft7">Quality factor will be normalized on the basis of normalization factor.</P>
