<P class="p0 ft2">Personalization</P>
<P class="p1 ft3">The goal of personalization is to present relevant and contextual content to the users of mobile app. Personalization will affect the following functionalities on the app:</P>
<P class="p2 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">Home Feed</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">Recommendations (content, tags, people), both in­app and mailers</SPAN></P>
<P class="p4 ft3">The idea of personalization is to tailor the content based on user’s interests rather than just providing generic and same content to all the users. Users are more likely to consume the content that matches their interests.</P>
<P class="p5 ft4">User’s interests can be categorised as follows:</P>
<P class="p6 ft6">Explicit:</P>
<P class="p7 ft3">These are the interests that a user specifies explicitly e.g. through a questionnaire (profile) or by explicitly following particular topics, people etc.</P>
<P class="p8 ft6">Implicit:</P>
<P class="p9 ft3">Implicit interests are derived from user behavior i.e. based on the actions that user performs on the app e.g. viewing, sharing, posting, upvote/downvote etc.</P>
<P class="p8 ft4">Both explicit and implicit interests are crucial for personalization.</P>
</DIV>
<DIV id="page_3">


<P class="p0 ft2" style="margin-top: 20px;">User Interest Profile</P>
<P class="p10 ft3">The first step to personalization is to capture user’s interests. As mentioned above, we’ll capture user interests both explicitly and implicitly and combine these two to have a unified view of user’s interest profile at any point of time.</P>
<P class="p11 ft3">Currently user interests are captured only in terms of topics (tags). We’ll keep a list of topics a user is interested in along with the affinity scores. Affinity score determines the level of interest of a user in a particular tag.</P>
<P class="p5 ft6">Sources for explicit interest:</P>
<P class="p12 ft4"><SPAN class="ft4">1.</SPAN><SPAN class="ft7">Profile information</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">2.</SPAN><SPAN class="ft7">Follow tags</SPAN></P>
<P class="p6 ft6">Sources for implicit interest:</P>
<P class="p12 ft4"><SPAN class="ft4">1.</SPAN><SPAN class="ft7">View question/discussion</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">2.</SPAN><SPAN class="ft7">Share question/discussion, answer/comment</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">3.</SPAN><SPAN class="ft7">Post question/discussion</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">4.</SPAN><SPAN class="ft7">Post answer/comment</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">5.</SPAN><SPAN class="ft7">Upvote/Downvote</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">6.</SPAN><SPAN class="ft7">View tag page</SPAN></P>
<P class="p13 ft3">Whenever a user performs any of the above activities, the corresponding tags will be associated to user’s interest profile.</P>
<P class="p5 ft8">Affinity Score</P>
<P class="p14 ft3">As a user consumes more and more content, a lot of tags will be associated with the user’s interest profile. We need some kind of scoring to differentiate tags that a user is genuinely interested in.</P>
<P class="p15 ft3">To compute the affinity score, we associate a score with each of the actions mentioned above. The score represents the relative intent of the users e.g. posting a question with a tag T is higher intent action than just viewing a question tagged with T. The scores from various actions on the same tag at different points of time are added up.</P>
<P class="p16 ft3">So at any point of time, we’ll have the list of tags that represent a user’s interest profile along with the affinity score of that user for each of the tags.</P>
