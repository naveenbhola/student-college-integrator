<P class="p35 ft8">Explicit Tags Priority</P>
<P class="p36 ft3">Tags that a user explicitly follows indicate a high level of intent and we want to give higher weightage to these tags.</P>
<P class="p8 ft6">Make sure explicit tags start in active profile</P>
