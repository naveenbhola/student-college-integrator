<P class="p25 ft15">Active Profile</P>
<P class="p26 ft16">As mentioned above, the purpose of affinity score is to differentiate between tags that a user is more interested in and the ones she is relatively less interested in. When filtering content for a user, we’ll only consider top N tags in her interest profile based on the affinity score. This will serve two purposes:</P>
<P class="p27 ft17">Reducing information overload</P>
<P class="p28 ft16">There might be hundreds (or even thousands) of tags for an active user in her interest profile. If we show all the content associated with these tags, user might get overwhelmed by the amount of information.</P>
<P class="p5 ft17">Showing highly relevant content</P>
<P class="p29 ft16">By using only the top tags that a user is really interested in, we’ll able to show highly relevant content to her while filtering out the noise (content based on tags a user might have only occasionally interacted with).</P>
<P class="p5 ft13">The logic for active profile is as follows:</P>
<P class="p30 ft13"><SPAN class="ft13">●</SPAN><SPAN class="ft18">Define a number N ­ maximum number of tags allowed in active profile</SPAN></P>
<P class="p31 ft20"><SPAN class="ft13">●</SPAN><SPAN class="ft19">If the number of tags in user’s interest profile is less than or equal to N, keep all the tags in the active profile irrespective of the score. This will help in quick bootstrapping of user’s profile</SPAN></P>
<P class="p32 ft16"><SPAN class="ft13">●</SPAN><SPAN class="ft21">If number of tags in user’s interest profile is greater than N, only top N tags based on affinity score constitute the active profile.</SPAN></P>
<P class="p33 ft16">User’s affinity score for different tags will keep on changing as the user will interact with the content, hence her active profile will also be dynamic.</P>
