<P class="p0 ft8">Home Feed Sorting</P>
<P class="p60 ft3">Before we present the feed to a user, we need to sort it so that the most relevant ones are on the top. For sorting, we have the following criteria:</P>
<P class="p2 ft4"><SPAN class="ft4">1.</SPAN><SPAN class="ft7">Recency</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">2.</SPAN><SPAN class="ft7">Interest match</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">3.</SPAN><SPAN class="ft7">Quality of content</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">4.</SPAN><SPAN class="ft7">Type of activity</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">5.</SPAN><SPAN class="ft7">Relation with the activity user</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">6.</SPAN><SPAN class="ft7">Network involvement</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">7.</SPAN><SPAN class="ft7">User’s previous involvement</SPAN></P>
<P class="p6 ft4">We’ll discuss each of the factors in detail.</P>
<P class="p35 ft31">Recency</P>
<P class="p61 ft4">More recent activities will have higher score.</P>
<P class="p35 ft31">Interest Match</P>
<P class="p62 ft3">Interest match score defines how relevant the content is with respect to user’s interest profile. The interest match can be computed as follows:</P>
<P class="p63 ft3"><SPAN class="ft4">●</SPAN><SPAN class="ft32">Find tags attached to the content which are also present in user’s interest profile (Intersection)</SPAN></P>
<P class="p40 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">Fetch user’s affinity score for each of the tags</SPAN></P>
<P class="p64 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">More specific tags will get higher weight while generic tags will get lower weight</SPAN></P>
<P class="p35 ft31">Quality of Content</P>
<P class="p65 ft3">The overall quality of the thread will also play a role in score calculation (hence sorting). We’ll maintain a quality score for each thread, which will depend on the following factors:</P>
<P class="p2 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">Level of the poster</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">No. of answers/comments</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">Average level of users providing answers/comments</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">No. of followers</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">No. of shares</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">No. of views</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">●</SPAN><SPAN class="ft5">Upvotes­Downvotes</SPAN></P>
