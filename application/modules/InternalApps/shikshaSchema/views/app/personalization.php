<P class="p0 ft4">The final quality score of the thread will be a linear combination of these factors.</P>
<P class="p35 ft31">Type of Activity</P>
<P class="p66 ft4">We can broadly classify the activities in following two categories:</P>
<P class="p67 ft3"><SPAN class="ft4">1.</SPAN><SPAN class="ft28">Activities where a new content is generated e.g. new question/discussion/answer/comment</SPAN></P>
<P class="p68 ft3"><SPAN class="ft4">2.</SPAN><SPAN class="ft28">Activities where some action is performed on existing content e.g. upvote/downvote, share, follow etc</SPAN></P>
<P class="p27 ft4">We’ll give a higher score where new content is created and lower score to other activities.</P>
<P class="p35 ft31">Relation with Activity User</P>
<P class="p69 ft3">The activity user can either be a friend or a followee. We’ll give higher score to the followee activity, since the user has explicitly chosen to follow her and a lower score to the friend’s activity.</P>
<P class="p27 ft31">Network Involvement</P>
<P class="p70 ft3">If people from user’s network have previously contributed to the thread, it’s more likely that the user will find it more interesting. That’s the basis behind taking network involvement into account while computing the score.</P>
<P class="p27 ft31">User’s Previous Involvement</P>
<P class="p71 ft3">If the user herself has previously contributed to a thread, she will be more interested in activities on that thread.</P>
