<P class="p0 ft8">Home Feed Architecture</P>
<P class="p45 ft4">For showing content on a user’s home feed, we have following two options:</P>
<P class="p6 ft6">Pull based (Fan­in)</P>
<P class="p52 ft3">In a pull based approach, we compute user’s home feed dynamically when requested. This will involve the following steps:</P>
<P class="p2 ft4"><SPAN class="ft4">1.</SPAN><SPAN class="ft7">Fetch user’s network (friends/followees)</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">2.</SPAN><SPAN class="ft7">Fetch user’s interest profile</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">3.</SPAN><SPAN class="ft7">Fetch questions/discussions that user is following</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">4.</SPAN><SPAN class="ft7">Fetch questions/discussions that user has contributed to</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">5.</SPAN><SPAN class="ft7">Fetch content based on network activity</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">6.</SPAN><SPAN class="ft7">Fetch content based on user’s interest profile</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">7.</SPAN><SPAN class="ft7">Fetch content based on questions/discussions user is following</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">8.</SPAN><SPAN class="ft7">Fetch content based on questions/discussions user has contributed to</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">9.</SPAN><SPAN class="ft7">Merge content from (5), (6), (7), (8)</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">10.</SPAN><SPAN class="ft27">Sort merged content based a sorting criteria</SPAN></P>
<P class="p3 ft4"><SPAN class="ft4">11.</SPAN><SPAN class="ft27">Show sorted content in user’s feed</SPAN></P>
<P class="p53 ft3">This approach is simple but not scalable. This requires a lot of data fetching and computations at run­time.</P>
<P class="p8 ft6">Push based (Fan­out)</P>
<P class="p54 ft3">In this approach, we keep home feeds of all the user’s pre­computed in memory and push activities into it as they happen.</P>
<P class="p55 ft3">For example, consider user U. Initially her home feed in memory will be empty. Whenever there is an activity for which user is eligible to receive feed, we’ll add this activity to user’s feed in memory. When user requests for home feed in app, we’ll just have to sort and materialise this in­memory view of her feed.</P>
<P class="p27 ft4">The steps for fan­out:</P>
<P class="p30 ft4"><SPAN class="ft4">1.</SPAN><SPAN class="ft7">A user does some activity on app. This triggers a call to backend API</SPAN></P>
<P class="p56 ft3"><SPAN class="ft4">2.</SPAN><SPAN class="ft28">From back­end API, add this activity to a message queue (RabbitMQ) along with activity parameters. Parameters are specific to activities.</SPAN></P>
<P class="p57 ft30"><SPAN class="ft4">3.</SPAN><SPAN class="ft29">A message queue worker picks up the activity and determines the intended audiences for this activity.</SPAN></P>
<P class="p40 ft4"><SPAN class="ft4">4.</SPAN><SPAN class="ft7">Add activity to in­memory feed of all intended audiences.</SPAN></P>
